"""
Operadores de comparação:

Operador de igualdade ==
Operador de Diferença !=
Operador maior que >
Operador manor que <
Operador maior ou igual >=
Operador menor ou igual <=
"""
a = 5
b = 9
#A saída das comparações abaixo são lógicas, True ou False.
print("Igualdade:",a == b)
print("Diferença:",a != b)
print("Maior que:",a > b)
print("Menor que:",a < b)
print("Maior ou igual:",a >= b)
print("Menor ou igual:",a <= b)