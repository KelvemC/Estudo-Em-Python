#A função casting converte os dados numéricos e de textos 
# para outros tipos de dados numéricos ou de textos.

#int
x = int(2)
y = int(2.8)
z = int('2')

print(x,y,z)

print("\n")

#float

a = float(2)
b = float(2.3)
c = float('5.5')
d = float('2')

print(a,b,c,d)

print("\n")

#str

t = 's1'
r = 2
z = 2.3

print("A variável t é do tipo ", type(t))
print("A variável r é do tipo ", type(r))
print("A variável z é do tipo ", type(z))

print("\n")
print("convertendo em str: ")
print("\n")
t = str('s1')
r = str(2)
z = str(2.3)

print("A variável t é do tipo ", type(t))
print("A variável r é do tipo ", type(r))
print("A variável z é do tipo ", type(z))